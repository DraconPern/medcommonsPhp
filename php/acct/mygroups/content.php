<h3>Groups and Interests</h3>
<p>MedCommons is a Web-connected system that also protects your privacy. This 
Groups and Keywords page is designed to let you know who shares the Worklist as 
member of your Group and who has access to your Personal Health Record as 
member of a Group. Users who are not listed in the PHR Access Group will need a 
PIN to access your CCRs.
</p><p>
Groups can also be a valuable resource to you and to the community. The Web 
offers new ways to harness the power of groups day-by-day. MedCommons helps 
users to protect their privacy while making Web resources more accessible and 
Web groups more effective. The Keywords section of this page allows you to 
associate with groups that are most likely to be of interest without exposing 
your identity. Keywords describe your interests directly or by association with 
public groups. The Keywords section allows you to tailor ads that may be shown 
on some MedCommons pages to match your interests and concerns. If this section 
is blank, the ads you will see will be as generic as the MedCommons web site 
itself.
